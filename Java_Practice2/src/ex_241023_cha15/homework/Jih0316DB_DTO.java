package ex_241023_cha15.homework;

public class Jih0316DB_DTO {
	private String id;
	private String password;
	private String email;

	public Jih0316DB_DTO(String id, String password, String email) {
		this.id = id;
		this.password = password;
		this.email = email;
	}

	public String getId() {
		return id;
	}

	public String getPassword() {
		return password;
	}

	public String getEmail() {
		return email;
	}
}
